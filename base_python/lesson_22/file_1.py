# bin(<integer>) санды экилик системага айландыруу
# 0b<экилик системадагы сан> бул экилик системадагы санды 10дук сисетемага айландырат

# oct(<integer>) санды сегиздик системага айландыруу
# 0o<сегиздик системадагы сан> бул сегиздик системадагы санды 10дук сисетемага айландырат

# hex(<integer>) санды он алтылык системага айландыруу
# 0x<он алтылык системадагы сан> бул он алтылык системадагы санды 10дук сисетемага айландырат
